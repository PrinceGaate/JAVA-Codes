package Github;

public class DecendingOrder {
    public static void main(String[]args){
        //Using For Loop
        for(int i=10;i>=1;i--){
            System.out.println(i);
        }

        //Using While Loop
        int j=10;
        while(j>=1){
            System.out.println(j);
            j--;
        }
    }
}
